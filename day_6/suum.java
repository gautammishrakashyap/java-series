// Write a program to calculate the sum of the first 10 natural numbers using a for loop.

// Output: Sum = 55
public class suum {
    public static void main(String[] args) {
        int sum = 0;
        for (int index = 1; index <= 10; index++) {
            sum = sum+index;
            
        }
        System.out.println(sum);
    }
    
}
